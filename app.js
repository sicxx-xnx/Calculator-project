operatorContainer.addEventListener("click",(e) => {
target = e.target    
switch (target.id) {
    case "plus": 
    if (operatorInput != "") {
     runEquation()   
    } else { operatorInput = "+"      
    }
    operatorInput = "+"  
     break;
    case "minus": if (operatorInput != "") {
     runEquation()   
    } else { operatorInput = "-"    
    }
    operatorInput = "-"    
     break;
    case "divide": if (operatorInput != "") {
     runEquation()   
    } else { operatorInput = "/"   
    }
    operatorInput = "/"    
     break;
    case "multiply": if (operatorInput != "") {
     runEquation()   
    } else { operatorInput = "X"    
    }
    operatorInput = "X"    
    break;
    default:
        break;
}    
})





